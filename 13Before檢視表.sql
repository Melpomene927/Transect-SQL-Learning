-- 建立廠商資料表
IF  EXISTS 
(
	SELECT * FROM sys.objects 
	WHERE object_id = OBJECT_ID(N'[dbo].[SUPPLIERS]') 
	AND type in (N'U')
) DROP TABLE [dbo].[SUPPLIERS]
CREATE TABLE [SUPPLIERS]
(
	SUPPLIERID NVARCHAR(10),
	SUPPLIERNAME NVARCHAR(50)
)
